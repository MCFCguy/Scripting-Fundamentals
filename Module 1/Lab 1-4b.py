""" This script takes a whole number from a user's input and prints out a table representing the outputs that result from multiplying it by factors of 2 (2,4,6,8,10) """

whole_num = int(input("Generate a multiplication table for: "))

print("_"*10)
print("Number:", whole_num)
print("2: " + str(whole_num*2))
print("4: " + str(whole_num*4))
print("6: " + str(whole_num*6))
print("8: " + str(whole_num*8))
print("10: " + str(whole_num*10))
print("_"*10)
